    private void logar() {        
        String sql = "select * from tbusuarios where login = ? and senha = ?";
        try {
          // as linhas abaixo preparam a conexao a consulta com banco de dados
          pst = conexao.prepareStatement(sql); // prepara o sql
          pst.setString(1, txtUsuario.getText()); // substitui as interrogações
          String captura = new String(txtSenha.getPassword());
          pst.setString(2, captura);
          // a linha abaixo executa a query
          rs = pst.executeQuery();
          // se existir usuario e senha correspondente 
          if (rs.next()) {
              TelaPrincipal principal = new TelaPrincipal();
              principal.setVisible(true);
              this.dispose(); // fechar tela login depois de logar
              conexao.close(); // fechar conexão com banco
          }
          else {
              JOptionPane.showMessageDialog(null, "Usuário e/ou senha inválido(s)");
          }
        }catch (Exception e) {
              JOptionPane.showMessageDialog(null, e);
        }
    }


